def lines(text: str) -> list[str]:
    normalized = dedent(text).strip("\n")
    if not normalized:
        return []
    return [f"{line}\n" for line in normalized.splitlines()]


def md(text: str) -> dict[str, object]:
    return {"cell_type": "markdown", "metadata": {}, "source": lines(text)}


def code(text: str) -> dict[str, object]:
    blocks = [dedent(block) for block in text.strip("\n").split("\n\n")]
    normalized = "\n\n".join(blocks)
    return {
        "cell_type": "code",
        "execution_count": None,
        "metadata": {},
        "outputs": [],
        "source": lines(normalized),
    }


def notebook(cells: list[dict[str, object]]) -> dict[str, object]:
    return {
        "cells": cells,
        "metadata": NOTEBOOK_METADATA,
        "nbformat": 4,
        "nbformat_minor": 5,
    }


